package tp05.ejercicio5;

public class AreaEmpresa {

	private String nombre;
	private int demoraTransmision;
	
	public AreaEmpresa(String nombre, int demoraTransmision) {
		this.nombre = nombre;
		this.demoraTransmision = demoraTransmision;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getDemoraTransmision() {
		return demoraTransmision;
	}

	public void setDemoraTransmision(int demoraTransmision) {
		this.demoraTransmision = demoraTransmision;
	}
	
	
	
	
	
}
